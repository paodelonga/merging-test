"""
### Sobre

Faça um Programa que
peça as 2 notas bimestrais
e mostre a média.

### Entrada

Digite a primeira nota:<br>
Digite a segunda nota:<br>
A média é:
"""

a = float(input("Digite a primeira nota: "))
b = float(input("Digite a segunda nota: "))
nota = ((a + b) / 2.00)
print(f"A média das notas é {nota}")
